const { expect } = require("chai");
const { it } = require("mocha");

it('adds two numbers', function(){
    expect(calculator(num1, num2)).to.equal(add(1, d))
})

it('subtract two numbers', function(){
    expect(calculator(num1, num2)).to.equal(sub(0, m))
})

it('multiply two numbers', function(){
    expect(calculator(num1, num2)).to.equal(mult(3, l))
})

it('divide two numbers', function(){
    expect(calculator(num1, num2)).to.equal(divi(6, e))
})